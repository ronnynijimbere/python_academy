#Slicing is a Python-specific concept for carving out a range of values from sequence types such as lists or strings.

name = 'Ronny'
surname = 'nijimbere'
word = 'Immaculate'
x = 'universe'
print(x[2::2])
print(name[1:3])
print(surname[2:6])
print(word[1:50])

s = 'sunshine'
print(s[1:5:2])
#'us'
print(s[1:5:1])
#'unsh'